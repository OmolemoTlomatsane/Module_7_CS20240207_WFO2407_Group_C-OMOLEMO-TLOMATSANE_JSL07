document.addEventListener('DOMContentLoaded', function () {
    const cardForm = document.getElementById('cardForm');
    const modal = document.getElementById('modal');
    const certificateContent = document.getElementById('certificateContent');
    const closeModal = document.querySelector('.close');
  
    // Hide the modal initially
    modal.style.display = 'none';
  
    cardForm.addEventListener('submit', function (e) {
      e.preventDefault();
  
      // üö® Get input values
      const studentNameInput = document.getElementById('studentName');
      const personalMessageInput =document.getElementById('personalMessage');
      const courseNameInput = document.getElementById('courseName'); 
  
      const studentName = studentNameInput.value;
      const personalMessage = personalMessageInput.value;
      const courseName = courseNameInput ? courseNameInput.value : "a course"; // Fallback to "a course" if no input
  
      if (studentName.trim() === '' || personalMessage.trim() === '') {
        alert('Please fill in all fields');
        return;
      }
  
      // üö® Generate certificate content dynamically
      certificateContent.innerHTML = `
      <h1> Certificate of Achievement </h1>
      <h3> This is to certifiy that </h3>
      <h3>${studentName}</h3>
      <h4>has almost completed the </h4>
      <h3> ${courseName} </h3>
      <h3> With legendary perserverance and world class bad-assery for never giving up üèÜ </h3>
      <img src ='logo.png' class='logo'>
      <h3>${personalMessage}</h3>
    `;
    
      //  Display the modal
      modal.style.display = 'block';
  
      // Clear the form inputs
      studentNameInput.value = '';
      personalMessageInput.value = '';
      if(courseNameInput) courseNameInput.value = '';
    });
  
    //  üö® Close the modal when the close button is clicked
    closeModal.addEventListener('click', function () {
      modal.style.display = 'none'
    });
  });
  